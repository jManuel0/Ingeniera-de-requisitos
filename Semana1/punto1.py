# area triangulo
print ("area triangulo")
baseTriangulo = input ("digita la base:")
alturaTriangulo = input ("digita la altura:")
areaTriangulo = (baseTriangulo * alturaTriangulo) / 2
print ("el area del triangulo es: ", areaTriangulo)

# area rectangulo
print ("area rectangulo")
baseRectangulo = input ("digita la base:")
alturaRectangulo = input ("digita la altura:")
areaRectangulo = baseRectangulo * alturaRectangulo
print ("el area del rectangulo es: ", areaRectangulo)

# area del terreno
print ("area total del terreno")
areaTotal = areaTriangulo + areaRectangulo
print ("el area total del terreno es: ", areaTotal)



